pub struct Padding {
    /// padding in rem to apply to top
    pub top: f32,

    /// padding in rem to apply to right
    pub right: f32,

    /// padding in rem to apply to bottom
    pub bottom: f32,

    /// padding in rem to apply to left
    pub left: f32,
}

impl CSS for Padding {
    fn css(&self) -> String {
        format!("padding: {}rem {}rem {}rem {}rem;", self.top, self.right, self.bottom, self.left)
    }
}

